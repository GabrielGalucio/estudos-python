# Criando funções com parametro em Python
# Por que saidas em forma de função? podemos pensar em uma função
# em uma f(x) como na matemática, o qual resultará em uma saída Y
# sendo assim f(x) -> f(return) -> y para ser mais especifico.

def somando_x(a):
    return a + 10

def subtraindo_y(b):
    return b - 2

print(somando_x(5))
print(somando_x(2))
print(somando_x(8))
print(subtraindo_y(4))
print(subtraindo_y(5))
print(subtraindo_y(10))

