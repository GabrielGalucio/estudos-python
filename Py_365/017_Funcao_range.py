# Alguns testes com a função range()

# range(começar, parar, passo)
# Podemos exemplificar de uma forma simples:
# start: o primeiro número de uma lista 
# stop: o ultimo número + 1
# pass: a distancia entre dois valores consecutivos


# Começamos demonstrando a estrutura de range()
print(range(10))

# Com o método lis() podemos listar os elementos em range()
print(list(range(10)))
print(list(range(3,7)))
print(list(range(1,10,2)))

