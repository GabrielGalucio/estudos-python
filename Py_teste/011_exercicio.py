# Tendo dados de entrada a altur de uma pessoa, construa um algoritmo que informe o seu peso
# ideal usando a seguinte formula: (72.7 * altura) - 58

altura_pessoa = (float(input("Digite aqui a sua altura")))
peso_ideal = (72.7 * altura_pessoa) - 58
print("Sua altura é: ",altura_pessoa," seu peso ideal é: ", peso_ideal)