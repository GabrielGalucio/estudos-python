# Conversão de int para float
var01 = 10
var01 = float(10)
print(var01)

# Conversão de float para int
var02 = 20.2
var02 = int(var02)
print(var02)

# Conversão para string
var03 = 10
var03 = str(var03)
print(type(var03))

# É possivel converter string para int e float, contando que esteja no mesmo formato
var04 = "123"
var05 = "2.2"
var04 = int(var04)
var05 = float(var05)
print(type(var04))
print(type(var05))


