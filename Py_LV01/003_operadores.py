# Operador aritmético de divisão (simples)

# O resultado entrega um número float
div01 = 4 / 2
div02 = 4.0 / 2
div03 = 4 / 2.0
print(div01)
print(div02)
print(div03)

# O resultado entrega um número inteiros por conta das "//" 
div04 = 4 // 2
div05 = 6 // 2
div06 = 10 // 2
print(div04)
print(div05)
print(div06) 
div07 = 4.0 // 2 # Nesse caso -> retorna número float da parte inteira da divisão.
print(div07)
div08 = 5 // 2 # # ATÇ: A divisão inteira arredonda o número ara baixo.
print(div08)     # resultado 2 em vez de 2.5


# Resto da divisão
