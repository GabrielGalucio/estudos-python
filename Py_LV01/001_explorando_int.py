# Testando o tipo int

num01 = 10   # É possivel colocar números inteiros
num02 = -20  # É possivel colocar números inteiros negativos
num03 = 0b10 # É possivel colocar os números binários também com prefixo 0b
num04 = 0o10 # É possivel colocar os números octais também com o prefixo 0o
num05 = 0x10 # É possivel colocar os números hexadecimais também com o prefio 0x
num06 = 1000000000000000000000000000000000000000000000000 # Números extensos são permitidos nessa tipagem


print(num01)
print(num02)
print(num03)
print(num04)
print(num05)
print(num06)

